bmi270_result_t bmi270_init(bmi270_t *const bmi270)
{
    if (bmi270 == NULL)
    {
        return BMI270_RESULT_ERROR;
    }

    memset(bmi270, 0, sizeof(bmi270));

    return BMI270_RESULT_SUCCESS;
}
